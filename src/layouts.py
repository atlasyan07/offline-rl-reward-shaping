FIXED_LAYOUTS = RAW_LAYOUTS


def _collect_goal_cells(layout_rows):
    cells = []
    for y, row in enumerate(layout_rows):
        for x, cell in enumerate(row):
            if cell in {"g", "G"}:
                cells.append((x, y))
    return cells


